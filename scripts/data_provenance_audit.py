"""Data provenance audit for StressBench.

Documents which datasets are real vs synthetic and why.
Produces a structured provenance table for §3.4 of the paper.

Key fact: All cross-mechanism transfer experiments use synthetic L2 order book data
generated by _synthetic_crossmech.py. The generators are calibrated to preserve
the statistics documented in the committed benchmark_results.csv from real data.

Outputs:
  results/tables/data_provenance.csv
  results/tables/data_provenance.json
"""

from __future__ import annotations

import csv
import json
from pathlib import Path

ROOT = Path(__file__).parents[1]
OUT = ROOT / "results/tables"
OUT.mkdir(parents=True, exist_ok=True)

